/**
 * Auto-generate file-manifest.json from public/data/*.json files.
 * Run via: node scripts/generate-manifest.mjs
 * Or automatically via: npm run generate-manifest
 */
import { readdirSync, writeFileSync } from 'fs';
import { join } from 'path';

const DATA_DIR = join(process.cwd(), 'public', 'data');
const MANIFEST_PATH = join(DATA_DIR, 'file-manifest.json');

try {
  const files = readdirSync(DATA_DIR)
    .filter(f => f.endsWith('.json') && f !== 'file-manifest.json')
    .sort((a, b) => {
      // Sort by date: DD-MM-YYYY
      const parseDate = (f) => {
        const parts = f.replace('.json', '').split('-');
        if (parts.length === 3) {
          return new Date(parts[2], parts[1] - 1, parts[0]).getTime();
        }
        return 0;
      };
      return parseDate(a) - parseDate(b);
    });

  const manifest = {
    files,
    generatedAt: new Date().toISOString(),
    autoGenerated: true
  };

  writeFileSync(MANIFEST_PATH, JSON.stringify(manifest, null, 2));
  console.log(`✅ Generated manifest with ${files.length} files:`);
  files.forEach(f => console.log(`   📄 ${f}`));
} catch (err) {
  console.error('❌ Failed to generate manifest:', err.message);
  process.exit(1);
}